
public class MainApp {

    public static void main(String[] args) {
        
        // Değişkenler
        // 1. Değişken Türü
        // 2. Değişken Adı
        // 3. Değişken Değeri

        // String Değişkenler
        // Karakter katarı

        String name = "Mehmet";
        String surname = "Bilmem";
        String stAge = "30";
        System.out.println(name);
        System.out.println(surname);
        System.out.println(stAge);

        // int -> tam sayılar
        int num1 = 40;
        int num2 = 666;
        int sumNumber = num1 + num2;
        System.out.println(sumNumber);

        // float -> küçük ondalıklı değer türleri
        float f1 = 10.5f;
        float f2 = 45.6f;
        float minFloat = f2 - f1;
        System.out.println(minFloat);

        // double -> büyük ondalıklı değer türleri
        double d1 = 55.9;
        double d2 = 60;
        double sumDouble = d1 + d2;
        System.out.println(sumDouble);


        // boolean -> true, false değer türleri
        boolean status1 = false;
        boolean status2 = true;
        boolean status3 = 35 > 36;

        // char type -> karakter
        char c1 = 'a';
        char c2 = '?';
        System.out.println(c1);


    }
    
}
